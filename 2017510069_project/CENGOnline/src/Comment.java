
public class Comment {
	private int ID;
	private String Comment;

	public Comment(int iD, String comment) {
		super();
		ID = iD;
		Comment = comment;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public String getComment() {
		return Comment;
	}

	public void setComment(String comment) {
		Comment = comment;
	}

}
